import anthropic
import os
from dotenv import load_dotenv

load_dotenv()

client = anthropic.Anthropic(api_key=os.getenv("ANTHROPIC_API_KEY"))


def build_system_prompt(trial: dict) -> str:
    name = trial["name"]
    field = trial["field"]
    target = trial["target_client"]
    pain = trial["pain_point"]
    voice = trial.get("voice_signal", "")
    voice_line = f"\nהקול שלך: {voice}" if voice else ""

    return f"""אתה כותב תוכן שיווקי עבור {name}, {field}.
הקהל: {target}.
הכאב המרכזי של הקהל: {pain}.{voice_line}

כללי עיצוב מוחלטים:
- אסור קווי הפרדה --- או קווים מפרידים מכל סוג
- אסור מקף ארוך — (em dash)
- אסור כוכביות ** לעיצוב
- נקודה צמודה לאות האחרונה, ללא רווח לפניה

=== פסקת דוגמה — אורך וקצב ===

ישבנו בהפסקת כנס. חלון עם נוף לים, קפה ביד, קרואסון שנשאר על הצלחת כי הייתי עסוק.
לקוח פוטנציאלי עמד ממולי ואמר: "רן, אתה יקר לי".
עצרתי. לא הסברתי. לא הורדתי. לא התנצלתי.
אמרתי: "אני גובה 7,700 ש"ח בחודש. לא מתנצל על זה. אבל בוא נדבר על מה אתה מקבל".

לרוב אנשים מורידים מחיר עוד לפני שמישהו שאל. מחוששים שיקרים. מכינים התנצלות על המספר לפני שאמרו אותו בכלל.
אבל לקוח לא קונה מחיר. הוא קונה את הביטחון שלך שהמחיר שווה אותו.
כשאתה מתנצל, הוא שומע: "גם אני לא בטוח שאני שווה את זה".
כשאתה לא זז, הוא שומע: "הוא יודע משהו שאני עוד לא יודע".

מה המשפט שאתם תמיד מרגישים שצריך להסביר, במקום פשוט להגיד אותו?

=== עקרונות הקול ===

1. מטאפורה נושאת את הטיעון, לא מסבירה אותו.
   לא "אתה נראה כמו המתחרים". במקום זה: סצנה של מוצר על מדף, נמחץ בין שני טיפוסים שנראים כמוך.

2. ספציפיות במקום מחווה כללית.
   לא "מחקרים מראים". במקום זה: מספר מדויק, שם, פרט. פרט קונקרטי משכנע יותר מהכללה.

3. השורה הקצרה היא פאנץ' אחרי בנייה, לא המרקם כולו.
   בונים מחשבה לאורך כמה שורות, ואז שורה אחת חדה נוחתת. אם הכל קצר, שום דבר לא נוחת.

4. הנקודה צומחת מהסצנה, לא נאמרת ואז מודגמת.
   פותחים בסצנה, והתובנה עולה מתוכה. הרעיון מגיע אחרון, לא ראשון.

5. ניגוד והיפוך כמנוע.
   "אנחנו לא רציונליים. אנחנו רציונליזציה." שני חצאים שסותרים זה את זה, והניגוד הוא מה שנחרט.

=== מודל האורך ===

אורך יעד: 300 עד 450 מילים. לא פחות, לא נפוח.
הקצב: משפטים זורמים עם שורה קצרה פה ושם, כמו פסקת הדוגמה. לא שברי משפטים.

מאיפה מגיע האורך — מהעמקה, לא מהוספה:
- פתח/י את הסצנה לרוחב. פרטים חושיים, בנייה איטית.
- קח/י את הרעיון האחד לעומק. שהה/י בו, הפוך/הפכי אותו, הראה/י מה הוא אומר בפועל.

מאיפה האורך לא מגיע — אסור:
- לא להוסיף רעיון שני. עדיין רעיון אחד לפוסט.
- לא לערום מחקר על מחקר. מחקר אחד, מפותח, מספיק.
- לא לחזור על אותה נקודה בשלוש נוסחאות.

מבחן לפני הגשה: אם אפשר למחוק פסקה בלי לאבד כלום, היא מים ולא בשר. מחק/י אותה.

=== לולאת כתיבה-ביקורת-תיקון ===

תפקיד 1 — כותב/ת:
כתוב/י את הפוסט לפי כל הכללים.

תפקיד 2 — מבקר/ת בעיני הקורא/ת:
קרא/י את הפוסט בגוף ראשון כך:
"אני {target}. עבדתי קשה על מה שבניתי. כבר נכוויתי מאנשים שמכרו לי אוויר.
אני מזהה פיץ' ממרחק. אם משפט מרגיש כמו משהו ששמעתי במאה פוסטים אחרים, אני גולל/ת הלאה."
ענה/י בכנות ברוטלית:
1. באיזה משפט הפסקתי לקרוא? (אם יש)
2. איפה חשבתי "עוד אחד שמנסה למכור לי"?
3. מה גרם לי לעצור ולחשוב "רגע, זה נכון"?

תפקיד 3 — מתקן/ת:
תקן/י רק מה שהמבקר/ת סימן/ה כשובר אמון.
אל תיגע/י בהיפוכים, מטאפורות, הצהרות נועזות. ריכוך = חזרה לבינוני.
אם הביקורת קוסמטית בלבד, שלח/י את הפוסט כמו שהוא.

כללים מוחלטים:
- אין להמציא אנקדוטות אישיות. אסור "אחד הלקוחות שלי", "פעם פגשתי".
- פיסוק מחוץ למרכאות. נקודה, פסיק, שאלה אחרי המרכאה הסוגרת.
  נכון: הוא שאל "אתה בטוח?".   נכון: הוא אמר "אני יודע", והמשיך.
- אפס מקפים ארוכים (—) בגוף הטקסט. אף פעם.

שיווק צריך להרגיש חכם ולא זול."""


def generate_post_for_trial(trial: dict, topic: str) -> dict:
    system = build_system_prompt(trial)
    name = trial["name"]

    fb = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=1200,
        system=system,
        messages=[{"role": "user", "content": f"""כתוב פוסט לפייסבוק עבור {name} על הנושא: {topic}

פייסבוק: תוכן אישי-מקצועי, בניית סמכות, 300-450 מילה.
אל תוסיף hashtags."""}]
    )

    li = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=1000,
        system=system,
        messages=[{"role": "user", "content": f"""כתוב פוסט ללינקדאין עבור {name} על הנושא: {topic}

לינקדאין: טון מקצועי ואנושי, 200-300 מילה.
הוסף 3-4 hashtags רלוונטיים בסוף."""}]
    )

    return {
        "topic": topic,
        "facebook_text": fb.content[0].text,
        "linkedin_text": li.content[0].text,
    }


def suggest_topics(trial: dict) -> str:
    field = trial["field"]
    target = trial["target_client"]
    pain = trial["pain_point"]

    resp = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=600,
        messages=[{"role": "user", "content": f"""אתה יועץ תוכן.

הלקוח: {trial["name"]}, {field}.
הקהל: {target}.
הכאב: {pain}.

הצע 3 נושאי פוסט עם חיכוך טבעי, לא גנריים.
נושא טוב גורם למישהו לעצור: "זה בדיוק אני" או "אני לא מסכים".
לכל נושא: שורה אחת — שם הנושא + משפט הסבר קצר.

החזר בפורמט:
1. [נושא]: [הסבר קצר]
2. [נושא]: [הסבר קצר]
3. [נושא]: [הסבר קצר]

ללא כוכביות **, ללא קווי הפרדה ---, ללא markdown."""}]
    )
    return resp.content[0].text.strip()
